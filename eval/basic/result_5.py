from qiskit import QuantumCircuit
def create_state_prep():
    """ Return a QuantumCircuit that prepares the binary state 1.
    """
    qc = QuantumCircuit(1)
    qc.x(0)
    return qc

qc = create_state_prep()
qc.draw()

